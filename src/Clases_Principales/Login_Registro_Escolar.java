    private void jButton_Iniciar_SesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Iniciar_SesionActionPerformed
        String Nombre_de_usuario = "Alejandro Machado";
        String Contraseña = "AlejM34";
        String Password_Vacio = "";

        String password = new String(this.jPasswordField_Contraseña.getPassword());

//CONDICIONAMOS QUE LOS CAMPOS NO PUEDAN ESTAR VACIOS
        if (jTextField_Nombre_de_usuario.getText().isEmpty() || password.equals(Password_Vacio)) {

            JOptionPane.showMessageDialog(this, "NO se pueden dejar espacios vacios, ingrese correctamente los datos");
        } else {

// VALIDAMOS EL ACCESO AL USUARIO
            if (this.jTextField_Nombre_de_usuario.getText().equals(Nombre_de_usuario) && password.equals(Contraseña)) {

                JOptionPane.showMessageDialog(this, "Datos correctamente ingresados. Bienvenido al sistema :D");

                Menu_Principal_ADMINISTRADOR Ventana = new Menu_Principal_ADMINISTRADOR();
                Ventana.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Los datos son incorrectos. Vuelva a intentarlo");
            }
        }
    }//GEN-LAST:event_jButton_Iniciar_SesionActionPerformed

