    private void jCheckBox_Mostrar_contraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox_Mostrar_contraseñaActionPerformed
        if (jCheckBox_Mostrar_contraseña.isSelected()) {
            jPasswordField_Contraseña.setEchoChar((char) 0);
            jLabel_Ojo_Oculto.setVisible(false);
           
        } else {
            jPasswordField_Contraseña.setEchoChar('*');
            jLabel_Ojo_Oculto.setVisible(true);
            
        }
    }//GEN-LAST:event_jCheckBox_Mostrar_contraseñaActionPerformed

